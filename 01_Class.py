class lokesh():
    a=2000
    b=2
    c=12
"""Method-I"""
print(lokesh().a)
print(lokesh().b)
print(lokesh().c)

"""Method-II"""
l=lokesh()
print(l.a)
print(l.b)
print(l.c)


